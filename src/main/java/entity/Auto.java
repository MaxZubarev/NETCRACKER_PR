package entity;

public class Auto {
    private int av_id;
    private int av_count;
    private int av_price;
    private int av_power;
    private int cat_id;



    public int getAv_id() {
        return av_id;
    }

    public void setAv_id(int av_id) {
        this.av_id = av_id;
    }

    public int getAv_count() {
        return av_count;
    }

    public void setAv_count(int av_count) {
        this.av_count = av_count;
    }

    public int getAv_price() {
        return av_price;
    }

    public void setAv_price(int av_price) {
        this.av_price = av_price;
    }

    public int getAv_power() {
        return av_power;
    }

    public void setAv_power(int av_power) {
        this.av_power = av_power;
    }

    public int getCat_id() {
        return cat_id;
    }

    public void setCat_id(int cat_id) {
        this.cat_id = cat_id;
    }
}
